"""Automated evaluation entry point.

Two ways to invoke:

  hlbench agent --env pendulum --budget 32 --max-turns 12 ...
        (preferred — via the unified ``hlbench`` CLI)

  python -m hlbench_harness --env pendulum --budget 32 ...
        (standalone fallback — same flags, useful for ad-hoc invocation
        without installing the console script)

The ``hlbench agent`` subcommand is implemented by ``hlbench_cli.main``
calling ``add_subparser_args()`` + ``run_with_args()`` from this
module, so flag definitions live here once.

Two backends, selected via ``--backend {claude,codex,kimi}`` (default
``claude``):

  - ``claude`` — Claude Code CLI; ``claude --print --resume <uuid>``.
  - ``codex``  — OpenAI Codex CLI 0.133+; ``codex exec`` then
                 ``codex exec resume <session-id>`` for follow-up
                 turns. The session id is auto-generated by codex and
                 scraped from the ``session_meta`` event on turn 0.
  - ``kimi``   — Moonshot Kimi Code CLI 0.6+; ``kimi -p`` then
                 ``kimi -S <session-id> -p`` for follow-up turns.
                 The session id is auto-generated and resolved via
                 stream-json scrape (primary) or
                 ``~/.kimi-code/session_index.jsonl`` lookup keyed by
                 workspace_dir (fallback).

Internally a run does:
  1. ``Server(env_id, runs_root, model, exp_id, config_overrides=...)``
  2. ``HlbenchHTTPServer`` on a background thread (port=0 by default
     so parallel runs don't collide).
  3. ``ClaudeAgent``, ``CodexAgent``, or ``KimiAgent`` with cwd =
     workspace.
  4. ``HarnessRunner.run()`` — blocks until budget exhausts /
     max_turns hit / N consecutive failures, then auto-finalizes.

Logs (under ``<run_dir>/logs/``):
  - ``agent_turns/turn_NNN.{stream.jsonl,json,txt,prompt.txt}``
  - ``harness_runner.json`` (per-turn timeline + final summary)
  - ``harness.log`` (Server-side lifecycle events)
  - ``agent.jsonl`` (operator-side activity per output.md §6.2)
  - ``../run.json`` (the headline; finalize is always called)
"""

from __future__ import annotations

import argparse
import logging
import sys
from pathlib import Path

from hlbench.core.server import Server
from hlbench.http_server import HlbenchHTTPServer
from hlbench_harness.claude_agent import (
    DEFAULT_ALLOWED_TOOLS,
    ClaudeAgent,
    ClaudeAgentConfig,
    find_claude_binary,
    new_session_id,
)
from hlbench_harness.codex_agent import (
    CodexAgent,
    CodexAgentConfig,
    find_codex_binary,
)
from hlbench_harness.kimi_agent import (
    KimiAgent,
    KimiAgentConfig,
    find_kimi_binary,
)
from hlbench_harness.runner import AgentLike, HarnessRunner, RunSummary

log = logging.getLogger("hlbench_harness")


# ---------------------------------------------------------------------------
# Public — used by both the standalone main() below and by
# hlbench_cli.main's "agent" subparser.
# ---------------------------------------------------------------------------


def add_subparser_args(p: argparse.ArgumentParser) -> None:
    """Attach the ~17 agent flags to ``p`` and set its handler to
    ``run_with_args``.

    Called by:
      - this module's standalone ``main()`` (so flags exist on the
        top-level argparse for ``python -m hlbench_harness ...``)
      - ``hlbench_cli.main:_build_parser()`` (so the same flags appear
        on ``hlbench agent ...``)

    Single source of truth for the agent CLI surface — adding a flag
    in one place lights it up in both invocations."""
    # ----- backend selection -----
    p.add_argument(
        "--backend", default="claude",
        choices=("claude", "codex", "kimi"),
        help="LLM agent CLI backend (default: claude). 'codex' uses "
             "the OpenAI Codex CLI ('codex exec' / 'codex exec resume'); "
             "'kimi' uses Moonshot Kimi Code CLI ('kimi -p' / 'kimi -S <id>').",
    )

    # ----- run identity -----
    p.add_argument("--env", default="pendulum", help="registered env id (default: pendulum)")
    p.add_argument(
        "--model-slug", default="claude-code-auto",
        help="run.json:model slug (default: claude-code-auto)",
    )
    p.add_argument(
        "--exp-id", default=None,
        help="distinguish multiple runs of the same (model_slug, env); "
             "auto-generated if omitted",
    )
    p.add_argument(
        "--runs-root", default="./runs",
        help="root for runs/<model>/<env>/<exp-id>/ (default: ./runs)",
    )

    # ----- budget overrides -----
    p.add_argument(
        "--budget", type=int, default=None,
        help="episode_budget override (default: env's default, usually 256)",
    )
    p.add_argument(
        "--max-episodes-per-submit", type=int, default=None,
        help="cap per single /submit call (default: env's default)",
    )

    # ----- harness loop -----
    p.add_argument(
        "--max-turns", type=int, default=12,
        help="hard cap on agent turns; auto-finalize on overrun (default: 12)",
    )
    p.add_argument(
        "--max-consecutive-failures", type=int, default=3,
        help="break the loop after N consecutive failed agent turns (default: 3)",
    )

    # ----- shared agent flags (apply to both backends) -----
    p.add_argument(
        "--model", default=None,
        help="model id passed to the backend (default: 'sonnet' for "
             "claude, 'gpt-5-codex' for codex, 'kimi-k2' for kimi)",
    )
    p.add_argument(
        "--turn-timeout", type=int, default=None,
        help="seconds before a single agent turn times out (default: 600 "
             "for claude, 900 for codex/kimi — these CLIs' first-call "
             "latency on macOS commonly busts 600)",
    )
    p.add_argument(
        "--session-id", default=None,
        help="reuse an explicit UUID for the agent session "
             "(default: auto-generated; codex/kimi backends mint a "
             "harness-side label here, the backend-internal id is "
             "scraped from turn 0)",
    )

    # ----- claude-only flags (--permission-mode / --allowed-tools kept
    # as deprecated aliases for one release) -----
    p.add_argument(
        "--claude-permission-mode", "--permission-mode",
        dest="claude_permission_mode",
        default="bypassPermissions",
        choices=("bypassPermissions", "acceptEdits", "default", "auto", "dontAsk", "plan"),
        help="claude --permission-mode (default: bypassPermissions). "
             "'--permission-mode' is a deprecated alias.",
    )
    p.add_argument(
        "--claude-allowed-tools", "--allowed-tools",
        dest="claude_allowed_tools",
        default="",
        help=(
            "comma-separated claude --allowedTools (default: "
            f"{','.join(DEFAULT_ALLOWED_TOOLS)}). "
            "'--allowed-tools' is a deprecated alias."
        ),
    )
    p.add_argument(
        "--claude-binary", default="claude",
        help="path/name of the claude binary (default: 'claude' on PATH)",
    )
    p.add_argument(
        "--no-require-claude", dest="require_claude", action="store_false",
        help="skip the PATH check for 'claude' (only useful if you're "
             "using --claude-binary to a custom binary, or running with "
             "--backend codex)",
    )

    # ----- codex-only flags -----
    p.add_argument(
        "--codex-binary", default="codex",
        help="path/name of the codex binary (default: 'codex' on PATH)",
    )
    p.add_argument(
        "--codex-sandbox-mode", default="workspace-write",
        choices=("read-only", "workspace-write", "danger-full-access"),
        help="codex -s sandbox policy (only used when "
             "--no-codex-bypass-approvals; default: workspace-write)",
    )
    p.add_argument(
        "--no-codex-bypass-approvals",
        dest="codex_bypass_approvals", action="store_false",
        help="don't pass --dangerously-bypass-approvals-and-sandbox to "
             "codex; falls back to --codex-sandbox-mode + interactive "
             "approvals (mostly for debugging)",
    )
    p.add_argument(
        "--no-require-codex", dest="require_codex", action="store_false",
        help="skip the PATH check for 'codex' (useful if --codex-binary "
             "points at a custom path)",
    )

    # ----- kimi-only flags -----
    p.add_argument(
        "--kimi-binary", default="kimi",
        help="path/name of the kimi binary (default: 'kimi' on PATH; "
             "kimi-code typically installs to ~/.kimi-code/bin/kimi)",
    )
    p.add_argument(
        "--no-kimi-yolo", dest="kimi_yolo", action="store_false",
        help="don't pass -y (yolo / auto-approve) to kimi; falls back "
             "to interactive approvals (mostly for debugging)",
    )
    p.add_argument(
        "--no-require-kimi", dest="require_kimi", action="store_false",
        help="skip the PATH check for 'kimi' (useful if --kimi-binary "
             "points at a custom path)",
    )

    # ----- http server -----
    p.add_argument("--host", default="127.0.0.1")
    p.add_argument(
        "--port", type=int, default=0,
        help="0 = ephemeral OS-assigned port (default; lets parallel runs coexist)",
    )

    # ----- misc -----
    p.add_argument("--log-level", default="INFO")
    p.set_defaults(
        require_claude=True, require_codex=True, require_kimi=True,
        codex_bypass_approvals=True, kimi_yolo=True, func=run_with_args,
    )


def run_with_args(args: argparse.Namespace) -> int:
    """Core agent-run logic. Takes a parsed Namespace (from either
    invocation path) and drives one full eval to completion. Returns
    a shell exit code (0 = run.json:outcome.status == "completed")."""
    logging.basicConfig(
        level=getattr(logging, args.log_level.upper(), logging.INFO),
        format="%(asctime)s %(name)s %(levelname)s: %(message)s",
    )

    # Backend-aware defaults for the shared flags.
    backend = args.backend
    if args.model is None:
        if backend == "claude":
            args.model = "sonnet"
        elif backend == "codex":
            args.model = "gpt-5-codex"
        else:  # kimi
            args.model = "kimi-k2"
    if args.turn_timeout is None:
        args.turn_timeout = 600 if backend == "claude" else 900

    # Binary presence checks (skippable per backend).
    if backend == "claude" and args.require_claude and find_claude_binary() is None:
        print(
            "error: 'claude' binary not found on PATH. "
            "Install Claude Code or pass --no-require-claude (test mode).",
            file=sys.stderr,
        )
        return 2
    if backend == "codex" and args.require_codex and find_codex_binary() is None:
        print(
            "error: 'codex' binary not found on PATH. "
            "Install OpenAI Codex CLI ('npm i -g @openai/codex' or "
            "see docs/dogfood.md), or pass --no-require-codex (test mode).",
            file=sys.stderr,
        )
        return 2
    if backend == "kimi" and args.require_kimi and find_kimi_binary() is None:
        print(
            "error: 'kimi' binary not found on PATH. "
            "Install Moonshot Kimi Code CLI (see "
            "https://moonshotai.github.io/kimi-code/) and ensure "
            "~/.kimi-code/bin is on your PATH, or pass --no-require-kimi "
            "(test mode).",
            file=sys.stderr,
        )
        return 2

    # 1. Spin up the per-run Server.
    overrides: dict[str, int] = {}
    if args.budget is not None:
        overrides["episode_budget"] = args.budget
    if args.max_episodes_per_submit is not None:
        overrides["max_episodes_per_submit"] = args.max_episodes_per_submit

    server = Server(
        env_id=args.env,
        runs_root=Path(args.runs_root).resolve(),
        model=args.model_slug,
        exp_id=args.exp_id,
        config_overrides=overrides or None,
    )
    log.info(
        "Server initialized: run_dir=%s exp_id=%s",
        server.run_dir, server.exp_id,
    )

    # 2. HTTP server on a background thread.
    http = HlbenchHTTPServer(server, host=args.host, port=args.port)
    http.start()
    try:
        url = f"http://{http.host}:{http.port}"
        log.info("HTTP server live: %s", url)

        # 3. Build the agent for the chosen backend.
        agent_log_dir = server.run_dir / "logs" / "agent_turns"
        agent: AgentLike
        if backend == "claude":
            session_id = args.session_id or new_session_id()
            agent = ClaudeAgent(
                workspace_dir=server.workspace_dir,
                http_url=url,
                log_dir=agent_log_dir,
                config=ClaudeAgentConfig(
                    model=args.model,
                    permission_mode=args.claude_permission_mode,
                    allowed_tools=(
                        tuple(args.claude_allowed_tools.split(","))
                        if args.claude_allowed_tools else DEFAULT_ALLOWED_TOOLS
                    ),
                    timeout_seconds=args.turn_timeout,
                    claude_binary=args.claude_binary,
                ),
                session_id=session_id,
            )
            log.info("Claude agent ready: session_id=%s", session_id)
        elif backend == "codex":
            agent = CodexAgent(
                workspace_dir=server.workspace_dir,
                http_url=url,
                log_dir=agent_log_dir,
                config=CodexAgentConfig(
                    model=args.model,
                    sandbox_mode=args.codex_sandbox_mode,
                    bypass_approvals=args.codex_bypass_approvals,
                    timeout_seconds=args.turn_timeout,
                    codex_binary=args.codex_binary,
                ),
                session_id=args.session_id,  # CodexAgent mints one if None
            )
            log.info(
                "Codex agent ready: harness_label=%s (codex id assigned on turn 0)",
                agent.session_id,
            )
        else:  # backend == "kimi"
            agent = KimiAgent(
                workspace_dir=server.workspace_dir,
                http_url=url,
                log_dir=agent_log_dir,
                config=KimiAgentConfig(
                    model=args.model,
                    yolo=args.kimi_yolo,
                    timeout_seconds=args.turn_timeout,
                    kimi_binary=args.kimi_binary,
                ),
                session_id=args.session_id,  # KimiAgent mints one if None
            )
            log.info(
                "Kimi agent ready: harness_label=%s (kimi session id "
                "resolved on turn 0)",
                agent.session_id,
            )

        # 4. Run the loop.
        runner = HarnessRunner(
            server=server,
            agent=agent,
            http_url=url,
            max_turns=args.max_turns,
            max_consecutive_failures=args.max_consecutive_failures,
            model_slug=f"{backend}:{args.model}",
        )
        summary = runner.run()
    finally:
        http.stop()

    _print_summary(summary, server)
    return 0 if summary.final_result and summary.final_result.get("status") == "completed" else 1


# ---------------------------------------------------------------------------
# Standalone entry — kept so ``python -m hlbench_harness`` still works
# even though the ``hlbench-agent`` console script was removed in favor
# of the unified ``hlbench agent`` subcommand.
# ---------------------------------------------------------------------------


def main(argv: list[str] | None = None) -> int:
    p = argparse.ArgumentParser(
        prog="python -m hlbench_harness",
        description=(
            "Drive a Claude Code, OpenAI Codex, or Moonshot Kimi Code "
            "session through one hlbench-pro run. Pass "
            "--backend {claude,codex,kimi} to pick. "
            "Preserves the agent's conversation across iterations via "
            "session resume. For the preferred invocation use "
            "``hlbench agent`` (same flags)."
        ),
    )
    add_subparser_args(p)
    args = p.parse_args(argv)
    return run_with_args(args)


def _print_summary(summary: RunSummary, server: Server) -> None:
    final = summary.final_result or {}
    print()
    print("=" * 60)
    print("hlbench agent run complete")
    print("=" * 60)
    print(f"  run_dir:           {server.run_dir}")
    print(f"  session_id:        {summary.session_id}")
    print(f"  turns:             {summary.n_turns}")
    print(f"  termination:       {summary.termination_reason}")
    if summary.total_cost_usd > 0:
        print(f"  total_cost_usd:    ${summary.total_cost_usd:.4f}")
        usage = summary.total_usage()
        if usage:
            in_t = usage.get("input_tokens", 0)
            out_t = usage.get("output_tokens", 0)
            print(f"  tokens (in/out):   {in_t} / {out_t}")
    print(f"  status:            {final.get('status')}")
    if final.get("status") == "completed":
        score = final.get("final_score")
        if score is not None:
            print(f"  final_score:       {score:.2f}")
        held_mean = final.get("held_out_mean_return")
        if held_mean is not None:
            print(f"  held_out_mean:     {held_mean:.2f}")
    elif final.get("error"):
        print(f"  error:             {final['error']}")
    print(f"  run.json:          {final.get('run_json_path')}")
    print()


if __name__ == "__main__":
    sys.exit(main())
