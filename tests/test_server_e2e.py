    assert meta["validation_errors"] == []
    assert meta["n_episodes_requested"] == 2
    assert meta["remaining_budget_before"] == 256
    assert meta["remaining_budget_after"] == 254
    assert meta["snapshot_files"] == ["policy.py"]
