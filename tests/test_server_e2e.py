    """Per CLAUDE.md invariant 5, workspace contains exactly:
    AGENTS.md, system/, feedback/. TASK.md is NOT staged — served via
    GET /task instead."""
    srv = Server(env_id="pendulum", runs_root=tmp_path / "runs")
