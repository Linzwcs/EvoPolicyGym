    assert not (ws / "TASK.md").exists()
    # Top-level workspace entries exactly = {AGENTS.md, system, feedback}.
    entries = {p.name for p in ws.iterdir()}
    assert entries == {"AGENTS.md", "system", "feedback"}, entries


